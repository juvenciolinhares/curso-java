
public class OperadoresDeAtribuicao {

	public static void main(String[] args) {
		
		int x = 6;
		System.out.println(x);
		
		 x += 3;
		 System.out.println(x);
		 
		 x -= 3;
		 System.out.println(x);
		 
		 x *= 3;
		 System.out.println(x);
		 
		 x /= 3;
		 System.out.println(x);
		 
		 x %= 3;
		 System.out.println(x);

	}

}
